const mongoose = require('mongoose');

const User = new mongoose.Schema({
    name: {
        type: String,
        require: true,
    },
    email: {
        type: String,
        unique: true,
        required: true,
        lowercase: true,
    },
    password: {
        type: String,
        required: true,
        select: false,
    }
},
{
    timestamps: true, 
});

mongoose.model('user', User);
